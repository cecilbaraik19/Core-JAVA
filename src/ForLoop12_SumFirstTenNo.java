//For Loop - Sum of first ten no.
public class ForLoop12_SumFirstTenNo {
    public static void main(String[] args) {
        int i,s=0;
        for(i=1;i<=10;i++)
        {
            System.out.println(i);
            s=s+i;
        }
        System.out.println("Sum of First Ten No. is :"+s);
    }
}
