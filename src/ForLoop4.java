//Another method of For Loop which printing from 1 to 10 and also from 20 to 11
public class ForLoop4 {
    public static void main(String[] args) {
        int i,j;
        for(i=1,j=20;i<=10;i++,j--)
        {
            System.out.println("Value of i = "+i+" , Value of j = "+j);
        }
    }
}
