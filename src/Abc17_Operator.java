// Increment and Decrement
public class Abc17_Operator {
    public static void main(String[] args) {
        int a = 5;
        System.out.println("Value of a in 1st Step   :"+a);
        System.out.println("Value of a in 2nd Step   :"+(++a));
        System.out.println("Value of a in 3rd Step   :"+a);
        System.out.println("Value of a in 4th Step   :"+(a++));
        System.out.println("Value of a in 5th Step   :"+a);
    }
}
